#!/usr/bin/env node
// Copies backend/lib/growthPipeline.js -> docs/js/growth-pipeline.js verbatim.
//
// Why this exists: GitHub Pages only serves the docs/ folder, so the
// frontend can't literally <script src="../backend/lib/growthPipeline.js">
// — it needs its own physical file on disk. Rather than hand-maintaining a
// second copy (which is exactly the duplication problem this file set out to
// fix), backend/lib/growthPipeline.js is the ONLY file anyone should ever
// edit; this script keeps docs/js/growth-pipeline.js byte-identical to it.
//
// Usage:
//   node scripts/sync-growth-pipeline.js          # writes the copy
//   node scripts/sync-growth-pipeline.js --check  # exits 1 if out of sync (used in CI)

const fs = require("fs");
const path = require("path");

const SRC = path.join(__dirname, "..", "backend", "lib", "growthPipeline.js");
const DEST = path.join(__dirname, "..", "docs", "js", "growth-pipeline.js");

const BANNER =
  "// ══════════════════════════════════════════════════════════════════════════\n" +
  "//  AUTO-GENERATED — DO NOT EDIT THIS FILE DIRECTLY.\n" +
  "//  This is an exact copy of backend/lib/growthPipeline.js, the single source\n" +
  "//  of truth for all interest-calculation math. Edit that file, then run:\n" +
  "//    node scripts/sync-growth-pipeline.js\n" +
  "//  (or `npm run sync-pipeline`) to regenerate this copy before committing.\n" +
  "// ══════════════════════════════════════════════════════════════════════════\n\n";

const srcContent = fs.readFileSync(SRC, "utf8");
const expected = BANNER + srcContent;

const checkOnly = process.argv.includes("--check");

if (checkOnly) {
  const current = fs.existsSync(DEST) ? fs.readFileSync(DEST, "utf8") : null;
  if (current !== expected) {
    console.error(
      "✗ docs/js/growth-pipeline.js is out of sync with backend/lib/growthPipeline.js.\n" +
        "  Run `npm run sync-pipeline` and commit the result."
    );
    process.exit(1);
  }
  console.log("✓ growth-pipeline.js is in sync.");
  process.exit(0);
}

fs.writeFileSync(DEST, expected);
console.log("✓ Synced backend/lib/growthPipeline.js -> docs/js/growth-pipeline.js");
